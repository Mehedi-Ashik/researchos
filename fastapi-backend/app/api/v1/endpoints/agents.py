@router.post("/task", response_model=Any, status_code=status.HTTP_201_CREATED)
def trigger_workspace_agent_task(
    payload: TaskCreate,
    db: Session = Depends(get_db),
    current_user: User = Depends(get_current_user)
) -> Any:
    """
    Triggers execution of a specific Multi-Agent workflow in the project workspace:
    - Supported task_types: 'literature_review', 'knowledge_graph', 'trend_analysis', 'research_proposal', 'fact_check'
    """
    try:
        service = AgentService(db)
        response = service.trigger_agent_task(payload)
        return response
    except Exception as e:
        raise HTTPException(
            status_code=status.HTTP_500_INTERNAL_SERVER_ERROR,
            detail=f"Agent workflow execution failed: {str(e)}"
        )


