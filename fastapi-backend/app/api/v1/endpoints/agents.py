@router.get("/project/{project_id}/graph", response_model=KnowledgeGraphResponse)
def get_project_knowledge_graph(
    project_id: UUID,
    db: Session = Depends(get_db),
    current_user: User = Depends(get_current_user)
) -> Any:
    """
    Retrieves the complete semantic Knowledge Graph (nodes & connections) extracted by the multi-agent graph layer.
    """
    try:
        repo = AgentRepository(db)
        nodes, relations = repo.get_project_graph(project_id)
        
        # Format lists using response models
        nodes_res = [GraphNodeResponse.model_validate(n) for n in nodes]
        relations_res = [GraphRelationResponse.model_validate(r) for r in relations]
        
        return KnowledgeGraphResponse(nodes=nodes_res, relations=relations_res)
    except Exception as e:
        raise HTTPException(
            status_code=status.HTTP_500_INTERNAL_SERVER_ERROR,
            detail=f"Failed to compile knowledge graph: {str(e)}"
        )


