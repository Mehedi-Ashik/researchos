@router.put("/project/{project_id}/nodes/{node_id}", response_model=GraphNodeResponse)
def update_project_node(
    project_id: UUID,
    node_id: UUID,
    payload: GraphNodeUpdate,
    db: Session = Depends(get_db),
    current_user: User = Depends(get_current_user)
) -> Any:
    """
    Updates label, type, or properties of an existing node.
    """
    try:
        node = db.query(KnowledgeGraphNode).filter(
            KnowledgeGraphNode.project_id == project_id,
            KnowledgeGraphNode.id == node_id
        ).first()
        if not node:
            raise HTTPException(status_code=404, detail="Node not found")
        
        if payload.label is not None:
            node.label = payload.label
        if payload.type is not None:
            node.type = payload.type
        if payload.properties is not None:
            node.properties = payload.properties
            
        db.add(node)
        db.commit()
        db.refresh(node)
        return GraphNodeResponse.model_validate(node)
    except HTTPException as he:
        raise he
    except Exception as e:
        db.rollback()
        raise HTTPException(
            status_code=status.HTTP_500_INTERNAL_SERVER_ERROR,
            detail=f"Failed to update node: {str(e)}"
        )


